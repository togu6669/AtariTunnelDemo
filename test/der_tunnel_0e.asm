; =============================================================================
; der_tunnel.asm  –  "Der Tunnel" gothic title screen
; ANTIC mode E (160x192, 4-colour bitmap, 2 bpp)
;
; Standalone:  mads.exe der_tunnel.asm -o:build/der_tunnel.xex
; Integrated:  icl "der_tunnel.asm"  in tunnel.asm  (ORG below is kept)
;
; Public entry point:
;   JSR SHOW_TITLE   – displays title, waits for any key, then returns.
;                      Saves and restores the caller's display list.
;
; Memory reserved:
;   TITLE_SCRN  = $6010 .. $7E0F   (7680 bytes, screen RAM)
;   TITLE_DLIST = $5F00 .. $5FCF   (display list, ~207 bytes)
;
; TITLE_SCRN starts at $6010 (not $6000) so that row 102 lands exactly at
; $7000 ($6010 + 102*40 = $7000), avoiding an ANTIC 4K boundary wrap.
;
; To add to tunnel.asm:
;   1.  icl "der_tunnel.asm"   anywhere after  ORG $2400
;   2.  JSR SHOW_TITLE          at the top of START, before the MAIN loop
; =============================================================================

        ORG $3800           ; safe standalone address; keep when icl'd

TITLE_SCRN  = $6010
TITLE_DLIST = $5F00

SDLSTL   = $0230
SDLSTH   = $0231
CH       = $02FC

_DL_SAVE_L .BYTE 0
_DL_SAVE_H .BYTE 0
_CP_PAGE_COUNT .BYTE 0
_CP_REM_COUNT  .BYTE 0
_CONV_TMP      .BYTE 0

; ---------------------------------------------------------------------------
; SHOW_TITLE
; ---------------------------------------------------------------------------
SHOW_TITLE
        LDA SDLSTL
        STA _DL_SAVE_L
        LDA SDLSTH
        STA _DL_SAVE_H

        ; clear from $6000, 31 pages = $1F00 bytes (covers $6000..$7EFF)
        LDA #0
        STA $FA
        LDA #$60
        STA $FB
        LDA #0          ; restore A=0 after setting $FB
        LDY #0
        LDX #$1F
_CLR_PAGE
        STA ($FA),Y
        INY
        BNE _CLR_PAGE
        INC $FB
        DEX
        BNE _CLR_PAGE

        JSR _COPY_BAND
        JSR _BUILD_DLIST

        LDA #$00
        STA $02C4           ; COLPF0
        STA $02C5           ; COLPF1
        LDA #$0F            ; COLPF2 = title pixels ($FF -> 2-bit pixel value 11)
        STA $02C6
        LDA #$00
        STA $02C8           ; COLBK = background

        LDA #<TITLE_DLIST
        STA SDLSTL
        LDA #>TITLE_DLIST
        STA SDLSTH

        LDA #$FF            ; clear keyboard buffer
        STA CH
_WAIT_KEY
        CMP CH              ; loop until CH != $FF (key pressed)
        BEQ _WAIT_KEY

        LDA _DL_SAVE_L
        STA SDLSTL
        LDA _DL_SAVE_H
        STA SDLSTH

        RTS

; ---------------------------------------------------------------------------
; _BUILD_DLIST  –  ANTIC mode-E DL at TITLE_DLIST
; 8 blank lines + 192 mode-E lines split at the 4K boundary ($7000):
;   line 0..101 from $6010  (102 lines * 40 = 4080 bytes → ends at $6FFF)
;   line 102..191 from $7000 ($6010 + 102*40 = $7000, no boundary crossing)
; ---------------------------------------------------------------------------
_BUILD_DLIST
        LDA #<TITLE_DLIST
        STA $FA
        LDA #>TITLE_DLIST
        STA $FB
        LDY #0

        LDA #$70            ; 8 blank scan-line instructions
        LDX #8
_BLD_BLK
        STA ($FA),Y
        INY
        DEX
        BNE _BLD_BLK

        LDA #$4E            ; first LMS: mode E from $6010
        STA ($FA),Y
        INY
        LDA #<TITLE_SCRN
        STA ($FA),Y
        INY
        LDA #>TITLE_SCRN
        STA ($FA),Y
        INY

        LDA #$0E            ; 101 mode-E lines (+ LMS line = 102)
        LDX #101
_BLD_F1
        STA ($FA),Y
        INY
        DEX
        BNE _BLD_F1

        LDA #$4E            ; second LMS at 4K boundary: $6010 + 102*40 = $7000
        STA ($FA),Y
        INY
        LDA #<(TITLE_SCRN+102*40)
        STA ($FA),Y
        INY
        LDA #>(TITLE_SCRN+102*40)
        STA ($FA),Y
        INY

        LDA #$0E            ; 89 mode-E lines (+ LMS line = 90; 102+90=192)
        LDX #89
_BLD_F2
        STA ($FA),Y
        INY
        DEX
        BNE _BLD_F2

        LDA #$41            ; JVB back to start of display list
        STA ($FA),Y
        INY
        LDA #<TITLE_DLIST
        STA ($FA),Y
        INY
        LDA #>TITLE_DLIST
        STA ($FA),Y

        RTS

; ---------------------------------------------------------------------------
; _COPY_BAND  –  copy and convert 1760 bytes of pre-rendered text to screen RAM
; Source: _BAND_DATA   Dest: TITLE_SCRN + 74*40  (row 74)
; 6 full pages + 224 remainder bytes
; _BAND_DATA is stored as mode-F 1 bpp. Each pair of source pixels becomes
; one mode-E 2 bpp pixel: 00 -> 00, anything else -> 11.
; ---------------------------------------------------------------------------
_COPY_BAND
        LDA #<_BAND_DATA
        STA $FA
        LDA #>_BAND_DATA
        STA $FB
        LDA #<(TITLE_SCRN + 74 * 40)
        STA $FC
        LDA #>(TITLE_SCRN + 74 * 40)
        STA $FD
        LDY #0
        LDX #6
        STX _CP_PAGE_COUNT
_CP_PAGE
        LDA ($FA),Y
        JSR _F_BYTE_TO_E
        STA ($FC),Y
        INY
        BNE _CP_PAGE
        INC $FB
        INC $FD
        DEC _CP_PAGE_COUNT
        BNE _CP_PAGE
        LDA #224     ; remaining bytes
        STA _CP_REM_COUNT
_CP_REM
        LDA ($FA),Y
        JSR _F_BYTE_TO_E
        STA ($FC),Y
        INY
        DEC _CP_REM_COUNT
        BNE _CP_REM
        
_CP_DONE
        RTS

_F_BYTE_TO_E
        PHA
        LSR @
        LSR @
        LSR @
        LSR @
        TAX
        LDA _F_TO_E_HI,X
        STA _CONV_TMP
        PLA
        AND #$0F
        TAX
        LDA _F_TO_E_LO,X
        ORA _CONV_TMP
        RTS

_F_TO_E_LO
