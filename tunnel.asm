START
        JSR INIT_SCREEN
MAIN
        JSR DRAW
        JSR WAIT_VBL
        INC PHASE
        JMP MAIN

; -------------------------
; Inicjalizacja ekranu
; -------------------------
INIT_SCREEN
        LDA #<SCREEN
        STA SAVMSC
        LDA #>SCREEN
        STA SAVMSC+1

        LDA #<DISPLAY_LIST
        STA SDLSTL
        STA DLISTL
        LDA #>DISPLAY_LIST
        STA SDLSTL+1
        STA DLISTL+1

        LDA #$22
        STA SDMCTL
        STA DMACTL

        LDA #$0E
        STA COLOR1
        LDA #$28
        STA COLOR2
        LDA #$00
        STA COLOR4

        RTS

; -------------------------
; Czekaj na kolejna ramke
; -------------------------
WAIT_VBL
        LDA RTCLOK60
WAIT_LOOP
        CMP RTCLOK60
        BEQ WAIT_LOOP
        RTS

; -------------------------
; Rysowanie (960 bajtow)
; -------------------------
DRAW
; ===== strona 1 ($0400-$04FF)
        LDY #0
LOOP1
        LDA DIST_TABLE,Y
        CLC
        ADC PHASE
        AND #$0F
        TAX
        LDA SHADE_TABLE,X
        STA SCREEN,Y

        INY
        BNE LOOP1

; ===== strona 2 ($0500-$05FF)
        LDY #0
LOOP2
        LDA DIST_TABLE+$100,Y
        CLC
        ADC PHASE
        AND #$0F
        TAX
        LDA SHADE_TABLE,X
        STA SCREEN+$100,Y

        INY
        BNE LOOP2

; ===== strona 3 ($0600-$06FF) ~256 bajty
        LDY #0
LOOP3
        LDA DIST_TABLE+$200,Y
        CLC
        ADC PHASE
        AND #$0F
        TAX
        LDA SHADE_TABLE,X
        STA SCREEN+$200,Y

        INY
        BNE LOOP3

; ===== strona 4 ($0700-$07BF) ~192 bajty
        LDY #0
LOOP4
        CPY #192
        BEQ END

        LDA DIST_TABLE+$300,Y
        CLC
        ADC PHASE
        AND #$0F
        TAX
        LDA SHADE_TABLE,X
        STA SCREEN+$300,Y

        INY
        JMP LOOP4

END
        RTS

