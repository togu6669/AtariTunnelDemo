TITLE_SCRN  = $6010

SDLSTL   = $0230
SDLSTH   = $0231
SDMCTL   = $022F
CH       = $02FC
DMACTL   = $D400
DLISTL   = $D402
DLISTH   = $D403

_DL_SAVE_L .BYTE 0
_DL_SAVE_H .BYTE 0
_DMA_SAVE  .BYTE 0

; ---------------------------------------------------------------------------
; SHOW_TITLE
; ---------------------------------------------------------------------------
SHOW_TITLE
        LDA SDLSTL
        STA _DL_SAVE_L
        LDA SDLSTH
        STA _DL_SAVE_H

        ; clear from $6000, 31 pages = $1F00 bytes (covers $6000..$7EFF)
        LDA #0
        STA $FA
        LDA #$60
        STA $FB
        LDA #0          ; restore A=0 after setting $FB
        LDY #0
        LDX #$1F
_CLR_PAGE
        STA ($FA),Y
        INY
        BNE _CLR_PAGE
        INC $FB
        DEX
        BNE _CLR_PAGE

        JSR _COPY_BAND

        LDA #$0F            ; COLPF1 = white pixels
        STA $02C5
        LDA #$00            ; COLPF2 = black background
        STA $02C6
        LDA #$00
        STA $02C8

        LDA #<TITLE_DLIST
        STA SDLSTL
        STA DLISTL
        LDA #>TITLE_DLIST
        STA SDLSTH
        STA DLISTH

        LDA #$22
        STA SDMCTL
        STA DMACTL

        JSR FADE

        LDA #$FF            ; clear keyboard buffer
        STA CH

_WAIT_KEY
        CMP CH              ; loop until CH != $FF (key pressed)
        BEQ _WAIT_KEY

        LDA _DL_SAVE_L
        STA SDLSTL
        STA DLISTL
        LDA _DL_SAVE_H
        STA SDLSTH
        STA DLISTH
        LDA _DMA_SAVE
        STA SDMCTL
        STA DMACTL

        RTS

; -------------------------
; Display list: tryb bitmapowy 320x192, 2 kolory
; -------------------------

TITLE_DLIST
        .BYTE $4F, a($6010)   ; pierwsza linia z LMS
        :101 .BYTE $0F        ; kolejne linie 1..102

        .BYTE $4F, a($7000)   ; nowy LMS od linii 102
        :89 .BYTE $0F         ; kolejne linie 104..191

        .BYTE $41, a(TITLE_DLIST)

; ---------------------------------------------------------------------------
; _COPY_BAND  -  copy 1760 bytes of pre-rendered text to screen RAM
; Source: _BAND_DATA   Dest: TITLE_SCRN + 74*40  (row 74)
; 6 full pages + 224 remainder bytes
; ---------------------------------------------------------------------------
_COPY_BAND
        LDA #<_BAND_DATA
        STA $FA
        LDA #>_BAND_DATA
        STA $FB
        LDA #<(TITLE_SCRN + 74 * 40)
        STA $FC
        LDA #>(TITLE_SCRN + 74 * 40)
        STA $FD
        LDY #0
        LDX #6
_CP_PAGE
        LDA ($FA),Y
        STA ($FC),Y
        INY
        BNE _CP_PAGE
        INC $FB
        INC $FD
        DEX
        BNE _CP_PAGE
        LDX #224     ; remaining bytes
        BEQ _CP_DONE
_CP_REM
        LDA ($FA),Y
        STA ($FC),Y
        INY
        DEX
        BNE _CP_REM
_CP_DONE
        RTS

