; Public entry point:
;   JSR SHOW_TITLE   – displays title, waits for any key, then returns.
;                      Saves and restores the caller's display list.
;
; Memory reserved:
;   TITLE_SCRN  = $6010 .. $7E0F   (7680 bytes, screen RAM)
;   TITLE_DLIST = $5F00 .. $5FCF   (display list, ~207 bytes)
;
; TITLE_SCRN starts at $6010 (not $6000) so that row 102 lands exactly at
; $7000 ($6010 + 102*40 = $7000), avoiding an ANTIC 4K boundary wrap.
;
; To add to tunnel.asm:
;   1.  icl "der_tunnel.asm"   anywhere after  ORG $2400
;   2.  JSR SHOW_TITLE          at the top of START, before the MAIN loop
; =============================================================================

        ORG $3800           ; safe standalone address; keep when icl'd

TITLE_SCRN  = $6010

SDLSTL   = $0230
SDLSTH   = $0231
SDMCTL   = $022F
CH       = $02FC
DMACTL   = $D400
DLISTL   = $D402
DLISTH   = $D403
RTCLOK60 = $0014

_DL_SAVE_L .BYTE 0
_DL_SAVE_H .BYTE 0
_DMA_SAVE  .BYTE 0

; ---------------------------------------------------------------------------
; SHOW_TITLE
; ---------------------------------------------------------------------------
SHOW_TITLE
        LDA SDLSTL
        STA _DL_SAVE_L
        LDA SDLSTH
        STA _DL_SAVE_H
        LDA SDMCTL
        STA _DMA_SAVE

        ; clear from $6000, 31 pages = $1F00 bytes (covers $6000..$7EFF)
        LDA #0
        STA $FA
        LDA #$60
        STA $FB
        LDA #0          ; restore A=0 after setting $FB
        LDY #0
        LDX #$1F
_CLR_PAGE
        STA ($FA),Y
        INY
        BNE _CLR_PAGE
        INC $FB
        DEX
        BNE _CLR_PAGE

        JSR _COPY_BAND

        LDA #$0F        ; COLPF1 = white pixels
        STA $02C5
        LDA #$00        ; COLPF2 = black background
        STA $02C6
        LDA #$00
        STA $02C8

        LDA #<TITLE_DLIST
        STA SDLSTL
        STA DLISTL
        LDA #>TITLE_DLIST
        STA SDLSTH
        STA DLISTH

        LDA #$22
        STA SDMCTL
        STA DMACTL

        JSR FADE

        LDA #$FF            ; clear keyboard buffer
        STA CH

_WAIT_KEY
        CMP CH              ; loop until CH != $FF (key pressed)
        BEQ _WAIT_KEY

        LDA _DL_SAVE_L
        STA SDLSTL
        STA DLISTL
        LDA _DL_SAVE_H
        STA SDLSTH
        STA DLISTH
        LDA _DMA_SAVE
        STA SDMCTL
        STA DMACTL

        RTS

; -------------------------
; Display list: tryb bitmapowy 320x192, 2 kolory
; -------------------------

TITLE_DLIST
        .BYTE $4F, a($6010)   ; pierwsza linia z LMS
        :101 .BYTE $0F        ; kolejne linie 1..102

        .BYTE $4F, a($7000)   ; nowy LMS od linii 102
        :89 .BYTE $0F         ; kolejne linie 104..191

        .BYTE $41, a(TITLE_DLIST)
; -------------------------

; ---------------------------------------------------------------------------
; FRAME_WAIT  –  wait for next frame (RTCLOK60 increments every 1/60th of a second)
; ---------------------------------------------------------------------------

FRAME_WAIT
        LDA RTCLOK60    ; wait for next frame (RTCLOK60 increments every 1/60th of a second)
_WAIT_LOOP
        CMP RTCLOK60
        BEQ _WAIT_LOOP
        RTS

; ---------------------------------------------------------------------------
; FADE  –  fade out by decrementing COLPF1 (white) to black
; ---------------------------------------------------------------------------

FADE    
        LDX #60      ; wait 1 second before starting fade (60 frames at 60Hz)
_SEC_WAIT_LOOP
        JSR FRAME_WAIT
        DEX            
        BNE _SEC_WAIT_LOOP

_FADE    
        LDX #5          ; 5 steps in the fade outer loop (0.1 second at 60Hz)
_5STEPS_LOOP
        JSR FRAME_WAIT
        DEX            ; decrement fade step counter
        BNE _5STEPS_LOOP

        DEC $02C5      ; decrement COLPF1 (white) to fade out
        BNE _FADE
        RTS

; ---------------------------------------------------------------------------
; _COPY_BAND  –  copy 1760 bytes of pre-rendered text to screen RAM
; Source: _BAND_DATA   Dest: TITLE_SCRN + 74*40  (row 74)
; 6 full pages + 224 remainder bytes
; ---------------------------------------------------------------------------
_COPY_BAND
        LDA #<_BAND_DATA
        STA $FA
        LDA #>_BAND_DATA
        STA $FB
        LDA #<(TITLE_SCRN + 74 * 40)
        STA $FC
        LDA #>(TITLE_SCRN + 74 * 40)
        STA $FD
        LDY #0
        LDX #6
_CP_PAGE
        LDA ($FA),Y
        STA ($FC),Y
        INY
        BNE _CP_PAGE
        INC $FB
        INC $FD
        DEX
        BNE _CP_PAGE
        LDX #224     ; remaining bytes
        BEQ _CP_DONE
_CP_REM
        LDA ($FA),Y
        STA ($FC),Y
        INY
        DEX
        BNE _CP_REM
        
_CP_DONE
        RTS

